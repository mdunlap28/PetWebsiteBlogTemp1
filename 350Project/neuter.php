<h1>Why Spay and Neuter Your Companion Animals</h1></br>
<h2>It's a lifesaver</h2>
<p>It's no myth, there is an overpopulation of unwanted pets nationwide and in our state. Every year more than 150,000 dogs and cats end up homeless in Washington State shelters, and sadly there are just not enough people who adopt from those shelters.</p>
<p>Some animals in shelters are saved from the streets and cruelty, while some are given up by their families. Countless others never make it to shelters and suffer without someone to care for them. Spaying or neutering pets prevents animals from being born accidentally, and is the most effective and humane way to save animals lives.</p>
<h2>Benefits for you and your pet</h2>
<p>Your companion will live a longer, healthier life and you will experience fewer headaches if you get him or her spayed or neutered.</p>
<p>Spaying and neutering reduces or eliminates:</p>
<ul>
<li>The odds of breast cancer and dangerous uterine infections in females and prostate problems and testicular cancer in males. </li>
<li>Frustration in resisting the natural urge to mate. Your companion will be less distracted, more easily trained, and a more contented member of your family. </li>
<li>The animal's need to roam in search of a mate, decreasing the chances that your pet will become lost, get into fights with other animals or be hit by a car. </li>
<li>Messy heat cycles in females and attracting unwanted males. </li>
<li>The tendency to bite. However, your pet will still be protective of his home and family even after being altered. Aggression is different from protectiveness. </li>
<li>Spraying, wailing, marking territory, or making inappropriate sexual approaches toward people or objects. </li>
<li>The extra expense for food or veterinary care in the event of an unexpected litter of puppies or kittens. </li>
</ul>
<p>In addition, many communities offer lower licensing fees and other benefits for spayed or neutered companion animals.</p>
<h2>Benefits for your community</h2>
<p>Spaying and neutering helps reduce the number of strays and unwanted animals in a community.</p>
<ul>
<li>Stray animals get into garbage cans, scare people, cause car accidents, and damage property. </li>
<li>Irresponsible or accidental breeding contributes to dog attacks and bites. </li>
<li>Some stray animals kill or injure wildlife.</li>
<li>Communities spend millions of tax dollars every year to provide care for unwanted, abandoned and neglected animals.</li>
</ul>
<h2>What is spay and neuter?</h2>
<ul>
<li>A spay is the surgical removal of a female animal's reproductive organs so she cannot become pregnant. <br /></li>
<li>A neuter is the surgical removal of a male animal's testicles so that he cannot impregnate a female. <br /></li>
<li>The surgeries are performed by a veterinarian while animals are under general anesthesia so that they do not feel any pain. An animal may experience some discomfort after the surgery, but most veterinarians will provide medication to help ease the soreness. <br /></li>
<li>When someone says an animal is "fixed" or "altered" that means the animal has been spayed or neutered.</li>
</ul>
<p>PAWS spays and neuters every animal adopted from our shelter, even those as young at eight-weeks-old. We also recommend that people have their pets altered as soon as possible. Cats can get pregnant as early as five months old, and dogs as early as six months old, and each can have two to three litters a year. For more questions about when to have your animal spayed or neutered, speak with your veterinarian.</p>
<h2>What are some myths?</h2>
<p>Pet guardians cite many reasons why they won't spay or neuter their animal. Among them:</p>
<ul>
<li><em>"My pet will become fat."</em><br />Too much food and lack of exercise makes a pet fat. If you monitor food intake and provide exercise, your pets will stay trim.</li>
<li><em>"He's purebred so he can't be fixed."</em><br />Purebreds and their offspring also end up homeless in shelters. Purebreds not spayed or neutered can also contribute to the problem of overpopulation. </li>
<li><em>"I will find good homes for all of the kittens (or puppies)."</em><br />If each of the great homes ready to welcome your pet's offspring would instead adopt from a shelter, they-and you-could potentially save the lives of deserving animals waiting for a new home.</li>
<li><em>"My pet is so special I want another pet just like her."</em><br />There is no guarantee that puppies and kittens will inherit their parents' best qualities. In fact, they may just as easily inherit the worst qualities.</li>
</ul>
<h2>What is the cost?</h2>
<p>The cost of a spay or neuter surgery depends on the weight, age and gender of your pet, whether or not your pet requires vaccinations and a number of other variables. It is important to remember, however, that it is a small, one-time cost compared to the numerous benefits it provides, and the number of unwanted issues that it will help you and your pet avoid.</p>
